import { access, mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const websiteRoot = path.resolve(__dirname, "..");
const sourcePath = path.resolve(
  websiteRoot,
  "..",
  "API",
  "MicroServices",
  "Payments",
  "Sources",
  "Resources",
  "BillingPlans.yaml",
);
const outputPath = path.resolve(
  websiteRoot,
  "lib",
  "generated",
  "billing-plan-features.gen.ts",
);

function humanizeFeatureCode(code) {
  return code
    .split("_")
    .map((segment) => {
      const lowercased = segment.toLowerCase();
      return lowercased.charAt(0).toUpperCase() + lowercased.slice(1);
    })
    .join(" ");
}

function parseFeatureTitles(source) {
  const featuresByPlan = {};

  let currentPlanCode = null;
  let insideFeaturesBlock = false;

  for (const line of source.split(/\r?\n/)) {
    const planMatch = line.match(/^  ([a-z0-9_]+):\s*$/);
    if (planMatch) {
      currentPlanCode = planMatch[1];
      featuresByPlan[currentPlanCode] = [];
      insideFeaturesBlock = false;
      continue;
    }

    if (!currentPlanCode) {
      continue;
    }

    if (/^\s{4}features:\s*$/.test(line)) {
      insideFeaturesBlock = true;
      continue;
    }

    if (!insideFeaturesBlock) {
      continue;
    }

    const featureMatch = line.match(/^\s{6}([a-z0-9_]+):\s*(true|false)\s*$/);
    if (featureMatch) {
      if (featureMatch[2] === "true") {
        featuresByPlan[currentPlanCode].push(humanizeFeatureCode(featureMatch[1]));
      }
      continue;
    }

    if (/^\s{4}[a-z_]+:/.test(line) || /^\s{2}[a-z0-9_]+:\s*$/.test(line)) {
      insideFeaturesBlock = false;
    }
  }

  return featuresByPlan;
}

async function main() {
  try {
    await access(sourcePath);
  } catch {
    try {
      await access(outputPath);
      console.warn(
        `Billing plans source not found at ${sourcePath}. Using committed generated file.`,
      );
      return;
    } catch {
      throw new Error(
        `Billing plans source not found at ${sourcePath}, and generated fallback is missing at ${outputPath}.`,
      );
    }
  }

  const source = await readFile(sourcePath, "utf8");
  const featuresByPlan = parseFeatureTitles(source);

  await mkdir(path.dirname(outputPath), { recursive: true });
  await writeFile(
    outputPath,
    [
      "/* eslint-disable */",
      "// This file is auto-generated by scripts/generate-billing-plan-features.mjs.",
      "// Source: API/MicroServices/Payments/Sources/Resources/BillingPlans.yaml",
      "",
      "export const BILLING_PLAN_FEATURE_TITLES_BY_CODE = " +
        `${JSON.stringify(featuresByPlan, null, 2)} as const;`,
      "",
    ].join("\n"),
    "utf8",
  );
}

main().catch((error) => {
  console.error("Failed to generate billing plan features:", error);
  process.exitCode = 1;
});
